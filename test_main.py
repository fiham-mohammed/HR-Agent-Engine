def test_create_and_get_memory():
    """Verifies manually creating memory (POST /api/v1/memory) and retrieving (GET /api/v1/memory)."""
    payload = {
        "user_id": "usr_test_1",
        "session_id": "sess_test_1",
        "content": "User prefers morning interview slots",
        "memory_type": "long_term",
        "significance_score": 8
    }
    response = client.post("/api/v1/memory", json=payload)
    assert response.status_code == 201
    data = response.json()
    assert data["user_id"] == "usr_test_1"
    assert data["content"] == "User prefers morning interview slots"
    assert data["memory_type"] == "long_term"
    assert data["significance_score"] == 8
    assert "id" in data

    get_res = client.get("/api/v1/memory?user_id=usr_test_1")
    assert get_res.status_code == 200
    memories = get_res.json()
    assert len(memories) >= 1
    assert memories[0]["content"] == "User prefers morning interview slots"


