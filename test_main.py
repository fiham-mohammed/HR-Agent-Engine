def test_audit_logs_append_only_enforcement():
    """Verifies that UPDATE or DELETE queries fail on the audit_logs table."""
    with get_db_connection() as conn:
        cursor = conn.cursor()
        cursor.execute("SELECT id FROM audit_logs LIMIT 1;")
        row = cursor.fetchone()
        assert row is not None
        log_id = row[0]

        with pytest.raises(sqlite3.IntegrityError) as excinfo:
            cursor.execute("UPDATE audit_logs SET user_id = 'hacked' WHERE id = ?;", (log_id,))
            conn.commit()
        assert "Updates are not allowed on the append-only audit_logs table" in str(excinfo.value)

        with pytest.raises(sqlite3.IntegrityError) as excinfo:
            cursor.execute("DELETE FROM audit_logs WHERE id = ?;", (log_id,))
            conn.commit()
        assert "Deletions are not allowed on the append-only audit_logs table" in str(excinfo.value)


